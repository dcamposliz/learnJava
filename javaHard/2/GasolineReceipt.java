public class GasolineReceipt {
	public static void main ( String[] args ) {
		System.out.println( "+------------------------+" );
		System.out.println( "|                        |" );
		System.out.println( "|      CORNER STORE      |" );
		System.out.println( "|                        |" );
		System.out.println( "| 2015-03-29  04:38PM    |" );
		System.out.println( "|                        |" );
		System.out.println( "| Gallons:       10.870  |" );
		System.out.println( "| Price/gallon: $ 2.089  |" );
		System.out.println( "|                        |" );
		System.out.println( "| Fuel total:  $ 22.71   |" );
		System.out.println( "|                        |" );
		System.out.println( "+------------------------+" );
	}
}

// notice the single "public class GasolineReceipt" above
// in Java, there can only be one public class
// and such class name MUST be the same as the file name

